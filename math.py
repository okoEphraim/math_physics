class Math:
    def __init__(self):
        pass

    def add(self, num1, num2):
        return num1 + num2

    def minus(self, num1, num2):
        return num1 - num2

    def multiply(self, num1, num2):
        return num1 * num2

    def divide(self, num1, num2):
        return num1 / num2

    def exponent(self, num1, num2):
        return num1 ** num2